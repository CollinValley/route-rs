//Most significant byte is 0th
#[derive(Eq, Clone, Copy, Hash, PartialEq)]
pub struct MacAddr {
    pub bytes: [u8; 6],
}

impl MacAddr {
    pub fn new(bytes: [u8; 6]) -> MacAddr {
        MacAddr { bytes }
    }
}

